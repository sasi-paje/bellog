/*
Tabela Principal:
- trx_route_invoice_delivery (resultados de entrega - já existe)

Nova Tabela:
- trx_route_invoice_delivery_attachment (anexos múltiplos)

Relacionamento:
- 1 delivery pode ter N anexos (1:N)

Campos da nova tabela:
- id: chave primária
- id_route_invoice_delivery: FK para trx_route_invoice_delivery
- attachment_type: 'receipt' (canhoto) ou 'nfd'
- file_name: nome original do arquivo
- file_path: path no storage
- file_url: URL pública (opcional)
- mime_type: tipo MIME do arquivo
- file_size: tamanho em bytes
- sort_order: ordem de exibição
- is_active: soft delete
- is_test: ambiente de teste
- created_at/updated_at: auditoria
*/