-- ============================================================
-- 1. CRIAR NOVA TABELA DE ANEXOS
-- ============================================================

CREATE TABLE IF NOT EXISTS trx_route_invoice_delivery_attachment (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_route_invoice_delivery BIGINT NOT NULL,
    attachment_type VARCHAR(20) NOT NULL CHECK (attachment_type IN ('receipt', 'nfd')),
    file_name VARCHAR(255) NOT NULL,
    file_path TEXT NOT NULL,
    file_url TEXT,
    mime_type VARCHAR(100),
    file_size BIGINT,
    sort_order INTEGER DEFAULT 0,
    is_active BOOLEAN DEFAULT true,
    is_test BOOLEAN DEFAULT false,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- ============================================================
-- 2. CRIAR ÍNDICES PARA PERFORMANCE
-- ============================================================

CREATE INDEX idx_attachment_delivery ON trx_route_invoice_delivery_attachment(id_route_invoice_delivery);
CREATE INDEX idx_attachment_type ON trx_route_invoice_delivery_attachment(attachment_type);
CREATE INDEX idx_attachment_active ON trx_route_invoice_delivery_attachment(is_active);

-- ============================================================
-- 3. CRIAR FOREIGN KEY
-- ============================================================

ALTER TABLE trx_route_invoice_delivery_attachment
ADD CONSTRAINT fk_attachment_delivery
FOREIGN KEY (id_route_invoice_delivery)
REFERENCES trx_route_invoice_delivery(id)
ON DELETE CASCADE;

