/*
================================================================================
MIGRAÇÃO: trx_route_invoice_delivery -> trx_attachment
================================================================================

-- Canhotos (receipt_image_path)
INSERT INTO trx_attachment (
    entity_type,
    entity_id,
    attachment_type,
    file_name,
    file_path,
    is_active,
    is_test,
    created_at,
    updated_at
)
SELECT
    'route_invoice_delivery' as entity_type,
    d.id as entity_id,
    'receipt' as attachment_type,
    COALESCE(SPLIT_PART(d.receipt_image_path, '/', -1), 'canhoto.pdf') as file_name,
    d.receipt_image_path as file_path,
    d.is_active,
    d.is_test,
    COALESCE(d.created_at, NOW()),
    COALESCE(d.updated_at, NOW())
FROM trx_route_invoice_delivery d
WHERE d.receipt_image_path IS NOT NULL
  AND d.receipt_image_path != ''
  AND d.receipt_image_path != '[]';

