/*
================================================================================
OPÇÃO A: Tabelas Específicas por Contexto
================================================================================

Prós:
- Simples inicialmente
- FK direta com integridade referencial

Contras:
- Fragmentação de código
- Mesma lógica repetida em várias tabelas
- Dificuldade para consultar anexos globally
- Maintenance overhead crescente
- Schema drift entre tabelas parecidas

Exemplo:
- trx_route_invoice_delivery_attachment
- trx_route_attachment
- trx_vehicle_attachment
- trx_user_attachment
- ... (cresce exponencialmente)

================================================================================
OPÇÃO B: Tabela Genérica Única (RECOMENDADA)
================================================================================

Prós:
- Uma única lógica de upload/download
- Consultas centralizadas
- Schema único para todos os anexos
- Mais fácil manter consistência
- Extensível para novos contextos sem criar tabelas
- Menos código boilerplate
- analytics e relatórios mais simples

Contras:
- entity_type precisa ser bem controlado
- Mais campos (mas compensado pela simplicidade)

================================================================================
DECISÃO: TABELA GENÉRICA - trx_attachment
================================================================================

Justificativa:
1. O sistema já tem múltiplos fluxos de anexo
2.Facilita manutenção e evolução
3.Permite implementar regras de negócio centralizadas
4.Melhor para buscar/filtrar anexos globalmente
5.Escalável para novos contextos sem mudança de schema

================================================================================
*/


-- ============================================================
-- TABELA GENÉRICA DE ANEXOS DO SISTEMA
-- ============================================================

CREATE TABLE IF NOT EXISTS trx_attachment (
    -- Identificação
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    -- Contexto do anexo (entidade pai)
    entity_type VARCHAR(50) NOT NULL,  -- 'route_invoice_delivery', 'route', 'vehicle', 'user', 'fiscal_invoice', 'note', etc.
    entity_id BIGINT NOT NULL,

    -- Tipo de anexo (classificação)
    attachment_type VARCHAR(30) NOT NULL,  -- 'receipt', 'nfd', 'document', 'image', 'signature', 'contract', etc.

    -- Informações do arquivo
    file_name VARCHAR(255) NOT NULL,
    original_file_name VARCHAR(255),  -- Nome original do arquivo antes de renomear
    file_path TEXT NOT NULL,  -- Path interno no storage
    file_url TEXT,  -- URL pública para acesso
    mime_type VARCHAR(100),  -- image/jpeg, application/pdf, etc.
    file_size BIGINT,  -- Tamanho em bytes

    -- Organização
    sort_order INTEGER DEFAULT 0,  -- Ordem de exibição
    description TEXT,  -- Descrição opcional do arquivo

    -- Controle
    is_active BOOLEAN DEFAULT true,
    is_test BOOLEAN DEFAULT false,

    -- Auditoria
    created_by BIGINT,  -- FK para usuário que enviou
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- ============================================================
-- ÍNDICES PARA PERFORMANCE
-- ============================================================

-- Busca por tipo de entidade
CREATE INDEX idx_attachment_entity ON trx_attachment(entity_type, entity_id);

-- Busca por tipo de anexo dentro de uma entidade
CREATE INDEX idx_attachment_type ON trx_attachment(entity_type, entity_id, attachment_type);

-- Busca por status
CREATE INDEX idx_attachment_active ON trx_attachment(is_active);

-- Busca por quem enviou
CREATE INDEX idx_attachment_created_by ON trx_attachment(created_by);

-- Busca por data de criação
CREATE INDEX idx_attachment_created_at ON trx_attachment(created_at DESC);

-- ============================================================
-- CONSTRAINTS
-- ============================================================

-- Garantir que entity_type seja válido (enum via CHECK)
ALTER TABLE trx_attachment
ADD CONSTRAINT chk_entity_type
CHECK (entity_type IN (
    'route',                    -- Rota
    'route_invoice_delivery',   -- Entrega de nota na rota
    'fiscal_invoice',           -- Nota fiscal
    'vehicle',                  -- Veículo
    'user',                     -- Usuário
    'provider',                 -- Fornecedor
    'customer',                 -- Cliente/Destino
    'route_occurrence',         -- Ocorrência de rota
    'delivery_occurrence'      -- Ocorrência de entrega
));

-- Garantir que attachment_type seja válido
ALTER TABLE trx_attachment
ADD CONSTRAINT chk_attachment_type
CHECK (attachment_type IN (
    'receipt',       -- Canhoto de entrega
    'nfd',          -- NFD/DANFE
    'document',    -- Documento genérico
    'image',       -- Imagem genérica
    'signature',   -- Assinatura
    'contract',    -- Contrato
    'license',     -- Licença/CNH
    'insurance',   -- Seguro
    'inspection',  -- Laudo/Inspeção
    'photo'        -- Foto
));

